import { newOxyPalette, OxyColorEx, OxyColors, type OxyPalette, OxyPaletteHelper } from '@/oxyplot'

/** Provides predefined palettes. */
export class OxyPalettes {
  /** The blue-white-red palette with 31 colors. */
  static blueWhiteRed31: OxyPalette

  /** The hot palette with 64 colors. */
  static hot64: OxyPalette

  /** The hue palette with 64 colors. */
  static hue64: OxyPalette

  /** Creates a black/white/red palette with the specified number of colors. */
  static blackWhiteRed(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(numberOfColors, OxyColors.Black, OxyColors.White, OxyColors.Red)
  }

  /** Creates a blue/white/red palette with the specified number of colors. */
  static blueWhiteRed(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(numberOfColors, OxyColors.Blue, OxyColors.White, OxyColors.Red)
  }

  /** Creates a 'cool' palette with the specified number of colors. */
  static cool(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(numberOfColors, OxyColors.Cyan, OxyColors.Magenta)
  }

  /** Creates a gray-scale palette with the specified number of colors. */
  static gray(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(numberOfColors, OxyColors.Black, OxyColors.White)
  }

  /** Creates a 'hot' palette with the specified number of colors. */
  static hot(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(
      numberOfColors,
      OxyColors.Black,
      OxyColorEx.fromRgb(127, 0, 0),
      OxyColorEx.fromRgb(255, 127, 0),
      OxyColorEx.fromRgb(255, 255, 127),
      OxyColors.White,
    )
  }

  /** Creates a palette from the hue component of the HSV color model. */
  static hue(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(
      numberOfColors,
      OxyColors.Red,
      OxyColors.Yellow,
      OxyColors.Green,
      OxyColors.Cyan,
      OxyColors.Blue,
      OxyColors.Magenta,
      OxyColors.Red,
    )
  }

  /** Creates a hue-based palette from magenta to red. */
  static hueDistinct(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(
      numberOfColors,
      OxyColors.Magenta,
      OxyColors.Blue,
      OxyColors.Cyan,
      OxyColors.Green,
      OxyColors.Yellow,
      OxyColors.Red,
    )
  }

  /** Creates a 'jet' palette with the specified number of colors. */
  static jet(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(
      numberOfColors,
      OxyColors.DarkBlue,
      OxyColors.Cyan,
      OxyColors.Yellow,
      OxyColors.Orange,
      OxyColors.DarkRed,
    )
  }

  /** Creates a rainbow palette with the specified number of colors. */
  static rainbow(numberOfColors: number): OxyPalette {
    return OxyPaletteHelper.interpolate(
      numberOfColors,
      OxyColors.Violet,
      OxyColors.Indigo,
      OxyColors.Blue,
      OxyColors.Green,
      OxyColors.Yellow,
      OxyColors.Orange,
      OxyColors.Red,
    )
  }

  // =================OxyPalettes.MatPlotLib================
  /**
   * Gets the cividis palette.
   */
  static cividis(numberOfColors: number = 256): OxyPalette {
    if (numberOfColors === cividisColors.length) {
      return newOxyPalette(cividisColors)
    }

    return OxyPaletteHelper.interpolate(numberOfColors, ...cividisColors)
  }

  /**
   * Gets the inferno palette.
   */
  static inferno(numberOfColors: number = 256): OxyPalette {
    if (numberOfColors === infernoColors.length) {
      return newOxyPalette(infernoColors)
    }

    return OxyPaletteHelper.interpolate(numberOfColors, ...infernoColors)
  }

  /**
   * Gets the magma palette.
   */
  static magma(numberOfColors: number = 256): OxyPalette {
    if (numberOfColors === magmaColors.length) {
      return newOxyPalette(magmaColors)
    }

    return OxyPaletteHelper.interpolate(numberOfColors, ...magmaColors)
  }

  /**
   * Gets the plasma palette.
   */
  static plasma(numberOfColors: number = 256): OxyPalette {
    if (numberOfColors === plasmaColors.length) {
      return newOxyPalette(plasmaColors)
    }

    return OxyPaletteHelper.interpolate(numberOfColors, ...plasmaColors)
  }

  /**
   * Gets the viridis palette.
   */
  static viridis(numberOfColors: number = 256): OxyPalette {
    if (numberOfColors === viridisColors.length) {
      return newOxyPalette(viridisColors)
    }

    return OxyPaletteHelper.interpolate(numberOfColors, ...viridisColors)
  }
}

