/**
 * Placement of the labels.
 */
export enum LabelPlacement {
  /**
   * Placed outside the bar.
   */
  Outside = 0,

  /**
   * Placed inside the bar.
   */
  Inside = 1,

  /**
   * Placed inside in the middle/center of the bar.
   */
  Middle = 2,

  /**
   * Placed inside at the base of the bar.
   */
  Base = 3,
}
