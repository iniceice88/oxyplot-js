/**
 * Specifies the layer for an Annotation.
 */
export enum AnnotationLayer {
  /**
   * Render the annotation below the gridlines of the axes.
   */
  BelowAxes,

  /**
   * Render the annotation below the series.
   */
  BelowSeries,

  /**
   * Render the annotation above the series.
   */
  AboveSeries,
}
