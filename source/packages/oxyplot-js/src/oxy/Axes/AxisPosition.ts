/**
 * Specifies the position of an Axis.
 */
export enum AxisPosition {
  /**
   * No position.
   */
  None,

  /**
   * Left of the plot area.
   */
  Left,

  /**
   * Right of the plot area.
   */
  Right,

  /**
   * Top of the plot area.
   */
  Top,

  /**
   * Bottom of the plot area.
   */
  Bottom,

  /**
   * All positions.
   */
  All,
}
