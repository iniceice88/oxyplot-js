/**
 * Defines the cursor type.
 */
export enum CursorType {
  /**
   * The default cursor
   */
  Default = 0,

  /**
   * The pan cursor
   */
  Pan,

  /**
   * The zoom rectangle cursor
   */
  ZoomRectangle,

  /**
   * The horizontal zoom cursor
   */
  ZoomHorizontal,

  /**
   * The vertical zoom cursor
   */
  ZoomVertical,
}
