/**
 * Defines the day of the week.
 */
export enum DayOfWeek {
  /**
   * Represents Sunday.
   */
  Sunday = 0,

  /**
   * Represents Monday.
   */
  Monday = 1,

  /**
   * Represents Tuesday.
   */
  Tuesday = 2,

  /**
   * Represents Wednesday.
   */
  Wednesday = 3,

  /**
   * Represents Thursday.
   */
  Thursday = 4,

  /**
   * Represents Friday.
   */
  Friday = 5,

  /**
   * Represents Saturday.
   */
  Saturday = 6,
}
